    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index($tag)
    {
        $guides = WinkPost::with(['tags' => function ($query) use ($tag) {
            $query->where('slug', '=', $tag);
        }])
            ->live()
            ->orderBy('publish_date', 'DESC')
            ->simplePaginate(12);

        $tag = WinkTag::where('slug', '=', $tag)->firstOrFail();

		return view('tags/index')->withGuides($guides)->withTag($tag);
    }

